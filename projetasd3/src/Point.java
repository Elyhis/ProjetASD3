public class Point {
    private int x, y;

    // Constructor
    // Complexity : O(1)
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters
    // Complexity : O(1)
    public int getX() {
        return x;
    }

    // Complexity : O(1)
    public int getY() {
        return y;
    }
}